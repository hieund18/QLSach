    private void btnThemGio1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemGio1ActionPerformed
        // TODO add your handling code here:
        dongChon = tbSach.getSelectedRow();
        if (dongChon != -1) {
            try {
                Sach s = SachDAL.listSach().get(dongChon);
                int soLuongDat = Integer.parseInt(txtSoLuongDat1.getText());
                if (soLuongDat <= s.getSoLuongCon()) {
                    GioHang gh = new GioHang();
                    gh.setMaTaiKhoan(DangNhapUI.username);
                    gh.setSachChon(s);
                    gh.setSoLuong(soLuongDat);
                    ArrayList<GioHang> listGh = GioHangDAL.listGioHang();
                    if (GioHangDAL.insert(listGh, gh)) {
                        JOptionPane.showMessageDialog(rootPane, "Thêm vào giỏ hàng thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        loadTableSach();
                        delTbSach();
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Số lượng đặt lớn hơn số lượng có", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, "Số lượng đặt phải là số", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn sản phẩm cần thêm", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemGio1ActionPerformed

    private void btnXoaGioHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaGioHangActionPerformed
        // TODO add your handling code here:
        try {
            dongChon = tbGioHang.getSelectedRow();
            if (dongChon != -1) {
                GioHang gh = GioHangDAL.listGioHangNow(DangNhapUI.username).get(dongChon);
                int rs = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa ?", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (rs == JOptionPane.YES_OPTION) {
                    if (GioHangDAL.delete(GioHangDAL.listGioHang(), gh)) {
                        JOptionPane.showMessageDialog(rootPane, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        loadTableGioHang();
                        loadTongTien();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn sản phẩm để xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger("HoaDonBan.DAT", ex.getMessage());
//            JOptionPane.showMessageDialog(rootPane, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnXoaGioHangActionPerformed

