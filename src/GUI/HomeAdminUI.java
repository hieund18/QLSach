    private void btnThem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem1ActionPerformed
        try {
            Sach s = new Sach();
            s.setMaSach(txtMaSach1.getText());
            s.setTenSach(txtTenSach1.getText());
            s.setTacGia(txtTacGia1.getText());
            s.setNhaXuatBan(txtNhaXuatBan1.getText());
            s.setNamXuatBan(Integer.parseInt(txtNamXuatBan1.getText()));
            s.setSoTrang(Integer.parseInt(txtSoTrang1.getText()));
            s.setDonGia(Double.parseDouble(txtDonGia1.getText()));
            s.setSoLuongCon(Integer.parseInt(txtSoLuongCon1.getText()));
            if (SachDAL.insert(SachDAL.listSach(), s)) {
                JOptionPane.showMessageDialog(rootPane, "Thêm sách thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadTableSach();
                delTextTbSach();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đúng định dạng số: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThem1ActionPerformed

    private void btnXoa1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa1ActionPerformed
        // TODO add your handling code here:
        if (SachDAL.listSach().size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Không có thông tin để xóa");
        } else {
            try {
                Sach s = new Sach();
                s.setMaSach(txtMaSach1.getText());
                if (SachDAL.delete(SachDAL.listSach(), s)) {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loadTableSach();
                    delTextTbSach();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoa1ActionPerformed

