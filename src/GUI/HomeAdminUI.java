    private void btnCapNhat2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhat2ActionPerformed
        // TODO add your handling code here:
        try {
            TaiKhoan tk = new TaiKhoan();
            tk.setMaTK(txtTaiKhoanQLTK.getText());
            tk.setMatKhau(txtMatKhauQLTK.getText());
            tk.setVaiTro(txtVaiTroQLTK.getText());
            int rs = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn cập nhật ?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (rs == JOptionPane.YES_OPTION) {
                if (TaiKhoanDAL.update(TaiKhoanDAL.listTaiKhoan(), tk)) {
                    JOptionPane.showMessageDialog(rootPane, "Cập nhật tài khoản thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    if (DangNhapUI.username.equals(tk.getMaTK())) {
                        JOptionPane.showMessageDialog(rootPane, "Vui lòng đăng nhập lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        DangNhapUI dn = new DangNhapUI();
                        dn.setVisible(true);
                        this.dispose();
                    }
                    loadTableTaiKhoan();
                    delTextTbTaiKhoan();
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đúng định dạng vai trò ! 0: Khách hàng. 1: Admin ", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCapNhat2ActionPerformed

    private void btnXoa2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa2ActionPerformed
        try {
            TaiKhoan tk = new TaiKhoan();
            tk.setMaTK(txtTaiKhoanQLTK.getText());
            int rs = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa ?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (rs == JOptionPane.YES_OPTION) {
                if (!DangNhapUI.username.equals(tk.getMaTK())) {
                    if (TaiKhoanDAL.delete(TaiKhoanDAL.listTaiKhoan(), tk)) {
                        JOptionPane.showMessageDialog(rootPane, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        loadTableTaiKhoan();
                        delTextTbTaiKhoan();
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Bạn không thể xóa tài khoản của chính mình", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnXoa2ActionPerformed

